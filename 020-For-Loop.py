# Usage of For Loops
for abc in "RANDOM":
    print(abc)
print("")


xyz = ["Nik", "Arr", "Sam", "Shivam"]
for names in xyz:
    print(names)
print("")


for nos in range(9, 20):
    print(nos)
print("")


for aa in range(5):
    if aa == 0:
        print("First Iteration")
        print("Yipee")

    else:
        print("Not first time")
        print("naah")